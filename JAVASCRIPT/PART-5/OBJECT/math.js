console.log(Math.pow(2,3));
/* Math.floor() ->round of to the nearset smallest integer value*/
console.log(Math.floor(-4.5)); //-5
console.log(Math.floor(3.9)); // 3
console.log(Math.floor(4.2)); // 4

/* Math.ceil() ->round of the nearest greater integer value */
console.log(Math.ceil(6.5)); // 7
console.log(Math.ceil(-6.8)); // -6
console.log(Math.ceil(7.1)); // 8

